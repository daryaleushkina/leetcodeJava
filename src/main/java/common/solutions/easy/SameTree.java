package common.solutions.easy;

import common.objects.TreeNode;

public class SameTree {
    //    Task â„–100 https://leetcode.com/problems/same-tree
    public boolean isSameTree(TreeNode p, TreeNode q) {
        if (p == null && q == null) {
            return true;
        }
        if (p == null || q == null) {
            return false;
        }
        if (p.val != q.val) {
            return false;
        }
        return isSameTree(p.left, q.left) && isSameTree(p.right, q.right);
    }

}
